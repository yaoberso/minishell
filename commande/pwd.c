#include "minishell.h"

void	ft_pwd(void)
{
	char	cwd[1024];

	val_ret = 0;
	if (getcwd(cwd, sizeof(cwd)) == NULL)
	{
		perror("getcwd");
		val_ret = 1;
		exit(1);
	}
	printf("%s\n", cwd);
}
